package com.rickjinny.leetcode;

/**
 * 674、最长连续递增序列
 */
public class T0674_longest_continuous_increasing_subsequence {

    public int findLengthOfLCIS(int[] nums) {
        int ans = 0;
        int n = nums.length;
        int start = 0;
        for (int i = 0; i < n; i++) {
            if (i > 0 && nums[i] <= nums[i - 1]) {
                start = i;
            }
            ans = Math.max(ans, i - start + 1);
        }
        return ans;
    }
}
